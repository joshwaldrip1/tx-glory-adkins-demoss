// Single source of truth for every stat a parent can fill in.
// The parent form, the profile display, and save/load logic all read from this.
// Storage shape: player.stats[groupKey][slugStat(label)] = "value string".
// A stat with no value is simply never stored and never displayed.

// Derive a safe, stable object key from a stat's display label.
// e.g. "AVG" -> "avg", "SB%" -> "sbpct", "K/BB" -> "k_bb", "2S+3" -> "2splus3".
export function slugStat(label) {
  return String(label)
    .toLowerCase()
    .replace(/%/g, "pct")
    .replace(/\+/g, "plus")
    .replace(/#/g, "num")
    .replace(/[\/\- ]+/g, "_")
    .replace(/[^a-z0-9_]/g, "")
    .replace(/_+/g, "_")
    .replace(/^_|_$/g, "");
}

